import math


class Solution:
    # @param A : tuple of integers
    # @return a list of integers
    def repeatedNumber(self, A):
        n = len(A)
        tmp = (n * (n + 1) // 2) - sum(A)
        p = 1
        for num in A: p *= num
        a = tmp / ((math.factorial(n) / p) - 1)

        b = a + tmp
        return [round(a), round(b)]


